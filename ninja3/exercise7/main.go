package main

import "fmt"

func main() {
	x := "James Bond"

	if x == "Juanita" {
		fmt.Println("It's Juanita")
	} else if x == "Panfilo" {
		fmt.Println("It's Panfilo")
	} else {
		fmt.Println("It's james Bond *_*")
	}
}
